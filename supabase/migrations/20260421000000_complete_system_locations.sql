-- system_locations テーブルの補完・修正
-- 以下の問題を一括修正します：
--   ① city カラムの NOT NULL 制約が以前のマイグレーションの構文バグで残っている可能性
--   ② updated_at の自動更新トリガーが未設定
--   ③ sort_order のインデックスが未設定
--   ④ target_group が NULL のレコードをデフォルト値で補完
--   ⑤ jma_code 等が NULL のレコードを city 名から自動補完

-- ① city カラムの NOT NULL 制約を確実に解除
ALTER TABLE public.system_locations ALTER COLUMN city DROP NOT NULL;

-- ② updated_at 自動更新トリガー用の共通関数（なければ作成）
CREATE OR REPLACE FUNCTION public.update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- system_locations にトリガーを設定
DROP TRIGGER IF EXISTS set_system_locations_updated_at ON public.system_locations;
CREATE TRIGGER set_system_locations_updated_at
  BEFORE UPDATE ON public.system_locations
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

-- user_locations にも同様のトリガーを設定
DROP TRIGGER IF EXISTS set_user_locations_updated_at ON public.user_locations;
CREATE TRIGGER set_user_locations_updated_at
  BEFORE UPDATE ON public.user_locations
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

-- ③ sort_order インデックス
CREATE INDEX IF NOT EXISTS idx_system_locations_sort_order ON public.system_locations (sort_order);

